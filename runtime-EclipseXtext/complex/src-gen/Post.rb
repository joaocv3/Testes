class Post   << HasAuthor 
    
# type String 	
def title 
    @title
end

def title=(value)
    @title = value
end
    
# type String 	
def content 
    @content
end

def content=(value)
    @content = value
end
    
# type Comment 	
def comments 
    @comments
end

def comments=(value)
    @comments = value
end
end
